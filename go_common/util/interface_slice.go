package util

func InterfaceSliceBool(dataSlice []bool) []interface{} {
	interfaceSlice := make([]interface{}, len(dataSlice))
	for i, data := range dataSlice {
		interfaceSlice[i] = data
	}
	return interfaceSlice
}

